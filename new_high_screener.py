def calc_penalty(df, pos_type, rvol=1.0, days_ago=0):
    c    = df["Close"].values
    h    = df["High"].values
    lo   = df["Low"].values
    rsi  = float(df["rsi"].iloc[-1])   if "rsi" in df.columns else 50
    r5   = (c[-1]/c[-6]-1)*100         if len(c)>=6  else 0
    r20  = (c[-1]/c[-21]-1)*100        if len(c)>=21 else 0
    r63  = (c[-1]/c[-64]-1)*100        if len(c)>=64 else r20   # 3개월 수익률

    rng3  = h[-3:] - lo[-3:]
    wick3 = h[-3:] - c[-3:]
    wr    = float(np.mean(wick3/rng3)) if np.all(rng3>0) else 0

    pen, rsn = 0, []

    # ── RSI 과열 — 75+ 기준 (모멘텀 시장에서 70~75는 추세 시작)
    rsi_t = {
        "PULLBACK_REBREAK":  (84, 80, 75),
        "TREND_CONTINUE":    (84, 80, 75),
    }.get(pos_type, (84, 80, 75))
    if   rsi > rsi_t[0]: pen+=18; rsn.append(f"RSI극과열({rsi:.0f})")
    elif rsi > rsi_t[1]: pen+=10; rsn.append(f"RSI과열({rsi:.0f})")
    elif rsi > rsi_t[2]: pen+=4;  rsn.append(f"RSI주의({rsi:.0f})")

    # ── RVOL 과열 페널티 (펌핑 신호) ──
    if   rvol > 7.0: pen+=15; rsn.append(f"RVOL펌핑({rvol:.1f}x)")
    elif rvol > 5.0: pen+=10; rsn.append(f"RVOL과열({rvol:.1f}x)")
    elif rvol > 3.0: pen+=5;  rsn.append(f"RVOL주의({rvol:.1f}x)")

    # ── days_ago 페널티 (오래된 신고가) ──
    if   days_ago >= 6: pen+=6; rsn.append(f"신고가{days_ago}일경과")
    elif days_ago >= 3: pen+=3; rsn.append(f"신고가{days_ago}일경과")

    # ── days_ago > 5 + 20일 수익률 음수 → 추가 페널티 ──
    if days_ago >= 5 and r20 < 0:
        pen+=8; rsn.append(f"신고가후하락({r20:.1f}%)")

    # 5일 급등
    if   r5 > 22: pen+=15; rsn.append(f"5일급등({r5:.0f}%)")
    elif r5 > 16: pen+=8;  rsn.append(f"5일과속({r5:.0f}%)")
    elif r5 > 12: pen+=3;  rsn.append(f"5일빠름({r5:.0f}%)")

    # 윗꼬리 (캔들 거절 신호)
    if   wr > 0.50: pen+=10; rsn.append("윗꼬리과다")
    elif wr > 0.38: pen+=5;  rsn.append("윗꼬리주의")

    # 추세진행형 20일 과속 추가 감점
    if pos_type == "TREND_CONTINUE" and r20 > 32:
        pen+=6; rsn.append(f"20일과속({r20:.0f}%)")

    # MA20 이격도 감점
    if "ma20" in df.columns:
        ma20_v = df["ma20"].iloc[-1]
        if not pd.isna(ma20_v) and float(ma20_v) > 0:
            dist_ma20 = (c[-1] / float(ma20_v) - 1) * 100
            if   dist_ma20 > 30: pen+=8;  rsn.append(f"MA20이격{dist_ma20:.0f}%")
            elif dist_ma20 > 20: pen+=4;  rsn.append(f"MA20이격{dist_ma20:.0f}%")

    # ── 3개월 과열 필터 ──
    r63_thres = {
        "PULLBACK_REBREAK": (50, 65, 85),
        "TREND_CONTINUE":   (65, 85, 110),
    }.get(pos_type, (50, 65, 85))
    if   r63 > r63_thres[2]: pen+=14; rsn.append(f"3개월극과열({r63:.0f}%)")
    elif r63 > r63_thres[1]: pen+=8;  rsn.append(f"3개월과열({r63:.0f}%)")
    elif r63 > r63_thres[0]: pen+=4;  rsn.append(f"3개월주의({r63:.0f}%)")

    return pen, rsn


