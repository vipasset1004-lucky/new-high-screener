def classify_position(df, nh):
    """
    4위치 분류 — 핵심: 파동 내 좌표 파악
    Minervini 기준 6번(저점 대비 30%+)도 함께 판별
    """
    close  = df["Close"].values
    high   = df["High"].values
    low    = df["Low"].values
    ma20   = df["ma20"].values

    rsi     = float(df["rsi"].iloc[-1])   if "rsi" in df.columns else 50
    ret_5d  = (close[-1]/close[-6]-1)*100  if len(close)>=6  else 0
    ret_20d = (close[-1]/close[-21]-1)*100 if len(close)>=21 else 0

    # 윗꼬리
    rng3  = high[-3:] - low[-3:]
    wick3 = high[-3:] - close[-3:]
    wick_r = float(np.mean(wick3/rng3)) if np.all(rng3>0) else 0

    # 52주 저점 대비 위치
    low_52w     = float(np.min(low[-HIGH_52W:])) if len(low)>=HIGH_52W else float(np.min(low))
    pos_pct     = (close[-1]-low_52w)/low_52w*100 if low_52w>0 else 999
    # Minervini 기준6: 저점 대비 30% 이상
    above_30pct = pos_pct >= 30.0

    # ── ④ 과열 — 극단값만 제외, 유형별 세부 조정은 calc_penalty에서 ──
    if (ret_5d>32 or rsi>88 or
        (ret_5d>24 and rsi>82) or
        (wick_r>0.55 and rsi>80)):
        return "OVERHEATED", pos_pct, ret_20d, above_30pct

    fb   = nh.get("first_breakout", False)
    days = nh.get("days_ago", 0)

    # 60일 박스권 여부
    p60h = float(np.max(high[-61:-1])) if len(high)>61 else float(np.max(high[:-1]))
    p60l = float(np.min(low[-61:-1]))  if len(low)>61  else float(np.min(low[:-1]))
    box_range = (p60h-p60l)/p60l*100 if p60l>0 else 999
    was_in_box = box_range < 35

    # ── 실질 눌림 확인: 고점→이후저점 패턴 (5%+ 조정) ──
    # 잘못된 방법: 90일 전체 max-min → 상승 자체를 눌림으로 오인
    # 올바른 방법: 구간 내 고점 이후에 실제로 5%+ 하락했는가
    nh_idx = len(close) - days - 1
    pb_s   = max(0, nh_idx - 90)
    had_pb = False
    if nh_idx > pb_s + 10:
        sub_c = close[pb_s:nh_idx]
        sub_h = high[pb_s:nh_idx]
        n = len(sub_h)
        if n >= 10:
            # 초반~중반(앞 80%)에서 고점 탐색 → 그 이후 저점과 비교
            peak_end  = max(5, int(n * 0.80))
            peak_idx  = int(np.argmax(sub_h[:peak_end]))
            peak_val  = float(sub_h[peak_idx])
            if peak_idx < n - 3 and peak_val > 0:
                trough_val    = float(np.min(sub_c[peak_idx:]))
                pullback_depth = (peak_val - trough_val) / peak_val * 100
                had_pb = pullback_depth >= 5.0

    # ── 추세 명확성 (200/150선 위) ──
    def _safe(key):
        v = df[key].iloc[-1] if key in df.columns else np.nan
        return float(v) if not pd.isna(v) else None
    ma150_v = _safe("ma150")
    ma200_v = _safe("ma200")
    in_uptrend = (ma150_v and ma200_v and
                  close[-1] > ma150_v and close[-1] > ma200_v and
                  ma150_v > ma200_v)

    # ① 바닥 탈출형 → 눌림재돌파로 통합 (백테스트 33% 승률 → 폐기)
    # 첫 돌파라도 had_pb(실질 조정)가 있으면 PULLBACK_REBREAK로 흡수
    if fb and (was_in_box or ret_20d <= 25) and ret_20d <= 40:
        if had_pb or was_in_box:
            return "PULLBACK_REBREAK", pos_pct, ret_20d, above_30pct
        return "OVERHEATED", pos_pct, ret_20d, above_30pct

    # ② 눌림 재돌파형 — 5%+ 실질 조정 후 재돌파
    if had_pb and not fb and ret_20d <= 40 and rsi < 80:
        return "PULLBACK_REBREAK", pos_pct, ret_20d, above_30pct

    # ③ 추세 진행형 — 강한 추세 중 신고가 경신 (조정 없이도 인정)
    if in_uptrend and ret_20d <= 50 and rsi < 82:
        return "TREND_CONTINUE", pos_pct, ret_20d, above_30pct

    # ③ 추세 진행형 — 완화 조건 (200선 위만 해도)
    if ma200_v and close[-1] > ma200_v and ret_20d <= 40 and rsi < 80:
        return "TREND_CONTINUE", pos_pct, ret_20d, above_30pct

    return "OVERHEATED", pos_pct, ret_20d, above_30pct


