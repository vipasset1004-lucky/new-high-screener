# STEP 4  VCP 수렴 강도 (Minervini VCP)
# ══════════════════���═════════════════════════��═══════════
def score_vcp(df, nh):
    """
    VCP(Volatility Contraction Pattern) 정밀 측정 (20점)

    Minervini 원칙:
      - 수축은 반드시 진행형이어야 함: 20%→12%→6% (각 수축이 이전보다 작아야)
      - 거래량도 각 수축마다 줄어야 함
      - 최소 2~3회 수축 확인

    구현:
      1) 돌파 직전 20일 구간을 5일씩 쪼개어 각 구간의 변동폭 측정
      2) 각 구간 변동폭이 이전 구간보다 줄어드는지 확인 (진행 수축)
      3) 거래량도 각 구간에서 감소 여부 확인
    """
    score  = 0
    detail = {}
    days_ago = nh.get("days_ago", 0)
    c  = df["Close"].values
    h  = df["High"].values
    lo = df["Low"].values
    v  = df["Volume"].values

    end_idx = max(1, len(c) - days_ago)

    # ── 진행 수축 측정 (5일 단위 4구간) ──
    seg = 5
    segs = []
    for i in range(4):
        s = max(0, end_idx - (i+1)*seg)
        e = max(0, end_idx - i*seg)
        if e > s:
            rng = float(np.mean(h[s:e] - lo[s:e]))
            vol = float(np.mean(v[s:e]))
            segs.append((rng, vol))

    segs.reverse()   # 오래된 → 최근 순

    progressive_price = 0   # 진행 수축 횟수 (가격)
    progressive_vol   = 0   # 진행 수축 횟수 (거래량)
    if len(segs) >= 2:
        for i in range(1, len(segs)):
            if segs[i][0] < segs[i-1][0] * 0.90:   # 10% 이상 수축
                progressive_price += 1
            if segs[i][1] < segs[i-1][1] * 0.90:
                progressive_vol += 1

    detail["progressive_price"] = progressive_price
    detail["progressive_vol"]   = progressive_vol

    # 진행 수축 점수
    if progressive_price >= 3:   score += 12
    elif progressive_price >= 2: score += 8
    elif progressive_price >= 1: score += 4

    if progressive_vol >= 2:     score += 5
    elif progressive_vol >= 1:   score += 3

    # ── BB 수축 보조 ──
    if "bb_w" in df.columns:
        bw = df["bb_w"].dropna()
        if len(bw) >= 30:
            rec = float(bw.iloc[-10:].mean())
            mx  = float(bw.iloc[-60:].max())
            bb_ratio = rec/mx if mx > 0 else 1.0
            if   bb_ratio < 0.40: score += 3
            elif bb_ratio < 0.55: score += 2
            elif bb_ratio < 0.70: score += 1
            detail["bb_ratio"] = round(bb_ratio, 2)

    # ── 거래량 드라이업 (Dry-Up) ──
    # O'Neil: 수렴 구간 거래량이 평균의 50% 미만 = 매도압력 없음
    if end_idx > 20:
        pre_vol  = v[max(0, end_idx-10):end_idx]
        base_vol = v[max(0, end_idx-40):max(0, end_idx-10)]
        if len(pre_vol) > 0 and len(base_vol) > 0:
            dry_ratio = float(np.mean(pre_vol)) / float(np.mean(base_vol))
            if dry_ratio < 0.50:
                score += 5; detail["vol_dryup"] = True
            elif dry_ratio < 0.70:
                score += 3; detail["vol_dryup"] = "partial"
            else:
                detail["vol_dryup"] = False
            detail["dry_ratio"] = round(dry_ratio, 2)

    # ── 베이스 기간 (O'Neil: 최소 6~8주 = 30~40 거래일) ──
    # 돌파 직전 얼마나 오래 박스권에서 에너지를 모았는지
    base_days = 0
    if end_idx > 20:
        look = min(120, end_idx - 1)
        sub_c = c[max(0, end_idx - look): end_idx]
        if len(sub_c) > 10:
            box_high = float(np.max(sub_c))
            lower_bound = box_high * 0.80   # 고점 대비 20% 이내 = 박스권
            for j in range(len(sub_c) - 1, -1, -1):
                if sub_c[j] >= lower_bound:
                    base_days += 1
                else:
                    break
    detail["base_days"] = base_days

    # 베이스 기간 가점
    if   base_days >= 60: score += 5
    elif base_days >= 40: score += 4
    elif base_days >= 30: score += 3
    elif base_days >= 20: score += 2
    elif base_days >= 10: score += 1

    # ── Tight Base 보너스 — 베이스 내 변동폭 < 15% = 에너지 응축 ──
    if base_days >= 20 and end_idx > base_days:
        base_sub = c[max(0, end_idx - base_days):end_idx]
        if len(base_sub) > 5:
            b_hi = float(np.max(base_sub))
            b_lo = float(np.min(base_sub))
            base_range_pct = (b_hi - b_lo) / b_lo * 100 if b_lo > 0 else 100
            if   base_range_pct < 10: score += 4; detail["tight_base"] = "super"
            elif base_range_pct < 15: score += 3; detail["tight_base"] = "tight"
            elif base_range_pct < 25: score += 1; detail["tight_base"] = "normal"
            else:                                  detail["tight_base"] = "loose"
            detail["base_range_pct"] = round(base_range_pct, 1)

    # ── Late Stage 패널티 — 지난 1년 신고가 경신 횟수로 추정 ──
    late_penalty = 0
    if len(h) >= 252:
        nh_count = 0
        for j in range(len(h) - 252, len(h) - 1):
            if j > 252 and h[j] >= float(np.max(h[max(0,j-252):j])) * 0.999:
                nh_count += 1
        if   nh_count >= 20: late_penalty = 8;  detail["stage"] = "late3+"
        elif nh_count >= 12: late_penalty = 4;  detail["stage"] = "late2"
        else:                                    detail["stage"] = f"early({nh_count})"
        detail["nh_count_1y"] = nh_count
        score -= late_penalty

    # ── VC 패턴 동시 확인 (#2: 변동성+캔들길이+거래량 3중 수축) ──
    if end_idx > 25:
        rng15v  = h[max(0,end_idx-15):end_idx] - lo[max(0,end_idx-15):end_idx]
        rng20v  = h[max(0,end_idx-20):end_idx] - lo[max(0,end_idx-20):end_idx]
        cv5     = float(np.mean(h[max(0,end_idx-5 ):end_idx] - lo[max(0,end_idx-5 ):end_idx]))
        cv10    = float(np.mean(h[max(0,end_idx-10):end_idx] - lo[max(0,end_idx-10):end_idx]))
        vv10    = float(np.mean(v[max(0,end_idx-10):end_idx]))
        vv20    = float(np.mean(v[max(0,end_idx-20):end_idx]))
        price_contr  = (float(np.mean(rng15v)) < float(np.mean(rng20v)) * 0.90
                        if len(rng15v) > 0 and len(rng20v) > 0 else False)
        candle_contr = cv5  < cv10 * 0.90 if cv10 > 0 else False
        vol_contr    = vv10 < vv20 * 0.90 if vv20 > 0 else False
        vc_confirmed = price_contr and candle_contr and vol_contr
        detail["vc_confirmed"] = vc_confirmed
        if vc_confirmed:
            score += 5
    else:
        detail["vc_confirmed"] = False

    return min(max(score, 0), 20), detail


