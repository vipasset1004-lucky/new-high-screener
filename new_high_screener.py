# predict_outcome — K-NN 유사 사례 기반 확률 예측
# ════════════════════════════════════════════════════════
_pred_db = None
_pred_db_path = None

def _load_pred_db(db_path="backtest_history.csv"):
    global _pred_db, _pred_db_path
    if _pred_db is not None and _pred_db_path == db_path:
        return _pred_db
    try:
        import pandas as pd
        df = pd.read_csv(db_path)
        # 수익률 컬럼이 있는 행만 유효 (백테스트 완료 행)
        df = df.dropna(subset=["ret_20d", "mdd_60d"])
        _pred_db = df
        _pred_db_path = db_path
        return df
    except Exception:
        return None


def predict_outcome(stock, db_path="backtest_history.csv", k=50):
    """
    K-NN으로 유사 과거 사례 k건 검색 → 승률/수익 통계 반환
    stock: analyze_stock() 결과 dict
    self-bias 방지: 동일 ticker의 과거 신호는 제외 (leave-one-out)
    """
    df = _load_pred_db(db_path)
    if df is None or len(df) < 10:
        return None

    pos_type   = stock.get("type", "")
    regime     = stock.get("market_regime", "")
    is_prime   = stock.get("is_prime", False)
    is_prime_s = stock.get("is_prime_s", False)
    rvol       = stock.get("rvol", 1.0)
    rsi        = stock.get("rsi", 50)
    score      = stock.get("score", 50)
    # self-bias 방지: 오늘 분석 종목의 과거 신호 제외
    own_ticker = str(stock.get("ticker", ""))

    # ① self-bias 제거 + 카테고리 필터
    mask = (
        (df["type"] == pos_type) &
        (df["regime"] == regime) &
        (df["ticker"].astype(str) != own_ticker)   # 동일 종목 제외
    )
    sub = df[mask]

    # ② 수치 필터 (rvol ±1.0, rsi ±15, score ±12)
    sub = sub[
        (sub["rvol"].between(rvol - 1.0, rvol + 1.0)) &
        (sub["rsi"].between(rsi - 15, rsi + 15)) &
        (sub["score"].between(score - 12, score + 12))
    ]

    # ③ PRIME 일치 우선 (가능하면)
    sub_prime = sub[sub["is_prime"] == is_prime]
    if len(sub_prime) >= 15:
        sub = sub_prime

    # ④ 표본 부족 시 카테고리 필터만 사용 (self-bias 제거 유지)
    if len(sub) < 10:
        sub = df[mask]
    if len(sub) < 5:
        sub = df[(df["type"] == pos_type) & (df["ticker"].astype(str) != own_ticker)]

    n = len(sub)
    if n == 0:
        return None

    # ⑤ 가장 가까운 k건 선택 (유클리디안 거리)
    if n > k:
        dist = (
            ((sub["rvol"]  - rvol)  / 2.0) ** 2 +
            ((sub["rsi"]   - rsi)   / 30.0) ** 2 +
            ((sub["score"] - score) / 20.0) ** 2
        )
        sub = sub.loc[dist.nsmallest(k).index]
        n = len(sub)

    win20   = round((sub["ret_20d"] > 0).mean() * 100, 1)
    avg20   = round(sub["ret_20d"].mean(), 2)
    p20pct  = round(sub["reached_20pct"].mean() * 100, 1) if "reached_20pct" in sub else None
    dd10    = round((sub["mdd_60d"] < -10).mean() * 100, 1)
    avg_mdd = round(sub["mdd_60d"].mean(), 1)

    # R/R 비율: avg20 / abs(avg_mdd)
    rr = round(avg20 / abs(avg_mdd), 2) if avg_mdd != 0 else None

    if   win20 >= 60 and avg20 >= 5 and (rr or 0) >= 0.3:
        rec = "BUY"
    elif win20 >= 50 and avg20 >= 2:
        rec = "WATCH"
    else:
        rec = "AVOID"

    return {
        "n"        : n,
        "win20"    : win20,
        "avg20"    : avg20,
        "p20pct"   : p20pct,
        "dd10"     : dd10,
        "avg_mdd"  : avg_mdd,
        "rr"       : rr,
        "rec"      : rec,
    }


def enrich_with_predict(results, db_path="backtest_history.csv"):
    """post_process 이후 predict_outcome을 각 종목에 붙임"""
    for r in results:
        try:
            pred = predict_outcome(r, db_path=db_path)
            r["predict"] = pred
        except Exception:
            r["predict"] = None
    return results


