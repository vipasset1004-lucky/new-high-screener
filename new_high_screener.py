def score_holding_rs(df, nh, index_df=None, pos_type=None):
    """
    유지력 (George & Hwang: 돌파 후 3일 버팀이 승률 핵심)
    Retest 패턴 (76% 승률 패턴)
    RS 상대강도 (O'Neil: RS 70 이상 필수)
    (25점)
    """
    score  = 0
    detail = {}
    c    = df["Close"].values
    v    = df["Volume"].values
    ma5  = df["ma5"].values
    ma10 = df["ma10"].values

    days_ago       = nh.get("days_ago", 0)
    breakout_price = nh.get("breakout_price", c[-1])
    breakout_tv    = nh.get("breakout_tv", 0)
    retest         = nh.get("retest", False)

    # ── 유지력 (15점) ──
    hold = 0
    detail["price_held"]  = False
    detail["vol_dried"]   = False
    detail["above_ma5"]   = False
    detail["days_held"]   = days_ago

    if days_ago >= 1:
        post_c  = c[-(days_ago):]
        post_tv = (c[-(days_ago):] * v[-(days_ago):])
        price_held = bool(np.all(post_c >= breakout_price * 0.97))
        vol_dried  = bool(len(post_tv)>0 and float(np.mean(post_tv)) < breakout_tv*0.72) if breakout_tv>0 else False
        above_ma5  = bool(c[-1] > ma5[-1]) if not np.isnan(ma5[-1]) else False
        above_ma10 = bool(c[-1] > ma10[-1]) if not np.isnan(ma10[-1]) else False

        if price_held: hold += 6
        if vol_dried:  hold += 5
        if above_ma5:  hold += 2
        if above_ma10: hold += 2

        # 3일 이상 버텼으면 만점 보정
        mult = min(1.0, days_ago / 3)
        hold = int(hold * mult)

        detail["price_held"] = price_held
        detail["vol_dried"]  = vol_dried
        detail["above_ma5"]  = above_ma5
    else:
        # 당일 신고가: 5일선 위면 기본 3점
        above_ma5 = bool(c[-1] > ma5[-1]) if not np.isnan(ma5[-1]) else False
        hold = 3 if above_ma5 else 1
        detail["above_ma5"] = above_ma5

    score += hold
    detail["hold_score"] = hold

    # ── Retest 패턴 보너스 (5점) ── 76% 승률
    if retest:
        score += 5; detail["retest"] = True
    else:
        detail["retest"] = False

    # ── RS 상대강도 — O'Neil 가중 분기 수익률 (5점) ──
    rs_score = 0
    rs_diff  = 0.0
    if index_df is not None:
        try:
            q1, q4    = 63, 252
            idx_close = index_df["Close"].values
            if len(c) >= q4 and len(idx_close) >= q4:
                # 최근 3개월 2배 가중
                s_q1   = (c[-1]  / c[-q1]          - 1) if c[-q1]          > 0 else 0
                s_q234 = (c[-q1] / c[-q4]           - 1) if c[-q4]          > 0 else 0
                i_q1   = (idx_close[-1]  / idx_close[-q1]  - 1) if idx_close[-q1]  > 0 else 0
                i_q234 = (idx_close[-q1] / idx_close[-q4]  - 1) if idx_close[-q4]  > 0 else 0
                rs_diff = (s_q1*2 + s_q234) - (i_q1*2 + i_q234)
            elif len(c) >= HIGH_52W and len(idx_close) >= HIGH_52W:
                rs_diff = (c[-1]/c[-HIGH_52W]-1) - (idx_close[-1]/idx_close[-HIGH_52W]-1)

            if   rs_diff > 0.40: rs_score = 5
            elif rs_diff > 0.20: rs_score = 3
            elif rs_diff > 0.05: rs_score = 2
            detail["rs_diff"] = round(rs_diff*100, 1)
        except:
            pass

    score += rs_score
    detail["rs_score"] = rs_score

    # ── MACD 방향성 보너스 (최대 3점) ──
    macd_bonus = 0
    if "macd_hist" in df.columns:
        mh = df["macd_hist"].dropna().values
        if len(mh) >= 4:
            if mh[-1] > 0 and mh[-4] <= 0:          # 골든크로스 직후
                macd_bonus = 3
            elif mh[-1] > 0 and mh[-1] > mh[-2] > 0:  # 히스토그램 확대
                macd_bonus = 2
            elif mh[-1] > 0:                          # 양전환 유지
                macd_bonus = 1
    score += macd_bonus
    detail["macd_bonus"] = macd_bonus

    # ── BB 위치 보너스 (최대 2점) ──
    bb_bonus = 0
    if "bb_up" in df.columns and "bb_mid" in df.columns:
        bb_up  = df["bb_up"].iloc[-1]
        bb_mid = df["bb_mid"].iloc[-1]
        price  = float(df["Close"].iloc[-1])
        if not (pd.isna(bb_up) or pd.isna(bb_mid)):
            if price >= float(bb_up) * 0.99:   # BB 상단 돌파 또는 근접
                bb_bonus = 2
            elif price > float(bb_mid):         # BB 중간선 위
                bb_bonus = 1
    score += bb_bonus
    detail["bb_bonus"] = bb_bonus

    # ── OBV 방향성 (최대 4점) — 매집인지 분산인지 ──
    obv_bonus = 0
    if "obv" in df.columns and "obv_ma20" in df.columns:
        obv_v    = df["obv"].values
        obv_ma   = df["obv_ma20"].values
        if not (np.isnan(obv_v[-1]) or np.isnan(obv_ma[-1])):
            obv_above_ma = obv_v[-1] > obv_ma[-1]
            obv_rising   = len(obv_v) >= 5 and obv_v[-1] > obv_v[-5]
            # OBV 신고가 (매집 최강 신호)
            obv_52h = float(np.max(obv_v[-252:])) if len(obv_v) >= 252 else float(np.max(obv_v))
            obv_ath  = obv_v[-1] >= obv_52h * 0.99
            if obv_ath and obv_rising:              obv_bonus = 4
            elif obv_above_ma and obv_rising:       obv_bonus = 3
            elif obv_above_ma:                      obv_bonus = 2
            elif obv_rising:                        obv_bonus = 1
    score += obv_bonus
    detail["obv_bonus"] = obv_bonus

    # ── A/D Line 방향성 (1점 보조) ──
    adl_bonus = 0
    if "adl" in df.columns and "adl_ma10" in df.columns:
        adl  = df["adl"].values
        adlm = df["adl_ma10"].values
        if not (np.isnan(adl[-1]) or np.isnan(adlm[-1])):
            if adl[-1] > adlm[-1] and len(adl) >= 3 and adl[-1] > adl[-3]:
                adl_bonus = 1
    score += adl_bonus
    detail["adl_bonus"] = adl_bonus

    # ── #3 기관 매집 흔적: Up-day vs Down-day Volume Ratio (최대 5점) ──
    updown_bonus = 0
    if "up_vol_20" in df.columns and "dn_vol_20" in df.columns:
        uv = float(df["up_vol_20"].iloc[-1]) if not pd.isna(df["up_vol_20"].iloc[-1]) else 0
        dv = float(df["dn_vol_20"].iloc[-1]) if not pd.isna(df["dn_vol_20"].iloc[-1]) else 1
        if dv > 0:
            ud_ratio = uv / dv
            detail["ud_ratio"] = round(ud_ratio, 2)
            if   ud_ratio >= 2.5: updown_bonus = 5
            elif ud_ratio >= 2.0: updown_bonus = 4
            elif ud_ratio >= 1.5: updown_bonus = 3
            elif ud_ratio >= 1.2: updown_bonus = 2
    score += updown_bonus
    detail["updown_bonus"] = updown_bonus

    # ── #9 MA20 이격도 < 10% 보너스 (이격도 회복형 돌파, 최대 3점) ──
    ma20_near_bonus = 0
    if "ma20" in df.columns:
        ma20_cur = df["ma20"].iloc[-1]
        if not pd.isna(ma20_cur) and float(ma20_cur) > 0:
            dist_ma20_pos = (float(df["Close"].iloc[-1]) / float(ma20_cur) - 1) * 100
            detail["ma20_dist_pct"] = round(dist_ma20_pos, 1)
            if   dist_ma20_pos <= 5.0:  ma20_near_bonus = 3
            elif dist_ma20_pos <= 10.0: ma20_near_bonus = 2
            elif dist_ma20_pos <= 15.0: ma20_near_bonus = 1
    score += ma20_near_bonus
    detail["ma20_near_bonus"] = ma20_near_bonus

    # ── #5 돌파확인: MA20 지지 유지 (돌파 후 20일선 위에서 버팀, 최대 2점) ──
    if days_ago >= 2 and "ma20" in df.columns:
        post_ma20 = df["ma20"].values[-days_ago:]
        post_c_m  = df["Close"].values[-days_ago:]
        valid_m   = ~np.isnan(post_ma20)
        if np.any(valid_m) and len(post_c_m) > 0:
            ma20_held = bool(np.all(post_c_m[valid_m] >= post_ma20[valid_m] * 0.98))
            detail["ma20_support"] = ma20_held
            if ma20_held:
                score += 2

    # ── Fibonacci 조정 깊이 (눌림재돌파형 전용, 최대 5점) ──
    # O'Neil: 이상적 조정은 38.2% 이내, 얕을수록 강세
    if pos_type == "PULLBACK_REBREAK":
        try:
            look = min(60, len(c) - 1)
            sub_h_fib = df["High"].values[-look:]
            sub_l_fib = df["Low"].values[-look:]
            prior_peak = float(np.max(sub_h_fib))
            pullback_trough = float(np.min(sub_l_fib))
            fib_depth = (prior_peak - pullback_trough) / prior_peak if prior_peak > 0 else 1.0
            detail["fib_depth"] = round(fib_depth * 100, 1)

            if   fib_depth <= 0.236: score += 5   # 매우 얕은 조정
            elif fib_depth <= 0.382: score += 4   # O'Neil 이상적
            elif fib_depth <= 0.500: score += 2   # 보통
            elif fib_depth <= 0.618: score += 1   # 깊음
        except:
            pass

    return min(score, 35), detail


