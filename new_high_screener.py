def post_process_results(results):
    """
    #4  RS 퍼센타일 랭킹      (스캔 유니버스 내 상위 20% 여부)
    #7  유동성 상위 30%        (avg_tv 기준 상대 랭킹)
    #E  섹터 동시성 점수       (같은 테마 신고가 2개↑ → 보너스, 단독 → 패널티)
    #10 combo_ok              기관급 최종 조합 필터 플래그
    """
    if not results:
        return results

    # ── RS 퍼센타일 (#4) ──────────────────────────────────
    rs_vals = [r.get("rs_diff", 0) for r in results]
    if rs_vals:
        rs_sorted = sorted(rs_vals, reverse=True)
        top20_th  = rs_sorted[max(0, int(len(rs_sorted) * 0.20) - 1)]
        for r in results:
            rv = r.get("rs_diff", 0)
            r["rs_top20"]    = bool(rv >= top20_th)
            r["rs_rank_pct"] = round(
                sum(1 for v in rs_vals if v <= rv) / len(rs_vals) * 100, 1
            )

    # ── 유동성 상위 30% (#7) ─────────────────────────────
    tv_vals = [r.get("avg_tv", 0) for r in results]
    if tv_vals:
        tv_sorted = sorted(tv_vals, reverse=True)
        top30_th  = tv_sorted[max(0, int(len(tv_sorted) * 0.30) - 1)]
        for r in results:
            r["liq_top30"] = bool(r.get("avg_tv", 0) >= top30_th)

    # ── 섹터 동시성 점수 (#E) ────────────────────────────
    # 테마별 종목 그룹 구성
    from collections import defaultdict
    theme_buckets = defaultdict(list)
    for r in results:
        for t in r.get("themes", []):
            theme_buckets[t].append(r)

    for r in results:
        themes       = r.get("themes", [])
        max_n        = 0     # 가장 많이 겹치는 테마의 종목 수
        is_leader    = False

        for t in themes:
            bucket = theme_buckets[t]
            n = len(bucket)
            if n > max_n:
                max_n = n
            # 이 테마에서 내가 최고 점수인지 확인
            if bucket and r["score"] >= max(x["score"] for x in bucket):
                is_leader = True

        # 점수 산정
        if is_leader and max_n >= 2:
            sec_bonus = 5   # 섹터 대장주
        elif max_n >= 3:
            sec_bonus = 3   # 섹터 동시성 강함
        elif max_n >= 2:
            sec_bonus = 2   # 섹터 동시성 있음
        else:
            sec_bonus = -1  # 단독 신고가 (약한 패널티)

        r["sector_count"]    = max_n
        r["is_sector_leader"] = is_leader
        r["sector_bonus"]    = sec_bonus

        # 섹터 보너스 점수 반영
        r["score"] = round(max(0, min(100, r["score"] + sec_bonus)), 1)

    # ── (B) 섹터 내부 상대강도 순위 (상위 30% 추가 보너스) ──
    for theme, bucket in theme_buckets.items():
        if len(bucket) < 2:
            continue
        # RS diff 기준 정렬 → 상위 30%에 추가 보너스
        by_rs = sorted(bucket, key=lambda x: x.get("rs_diff", 0), reverse=True)
        top_n = max(1, round(len(by_rs) * 0.30))
        for i, r in enumerate(by_rs):
            if i < top_n:
                if not r.get("_sector_rs_bonus_applied"):
                    r["sector_rs_top30"] = True
                    r["score"] = round(min(100, r["score"] + 2), 1)
                    r["_sector_rs_bonus_applied"] = True  # 중복 방지

    # 기본값 세팅
    for r in results:
        r.setdefault("sector_rs_top30", False)
        r.pop("_sector_rs_bonus_applied", None)

    # 섹터 보너스 반영 후 등급 재계산
    for r in results:
        g, gl = grade(r["score"], r["type"])
        r["grade"] = g
        r["grade_label"] = gl

    # ── CB_SCORE 최종 보정 (RS 퍼센타일 + 섹터 보너스) ──────
    for r in results:
        raw = r.get("cb_score_raw", 0)
        if raw == 0:
            r["cb_score"] = 0
            continue
        rs_add = 0
        rp = r.get("rs_rank_pct", 0)
        if   rp >= 80: rs_add = 15
        elif rp >= 60: rs_add = 8
        elif rp >= 40: rs_add = 3
        sec_add = max(0, min(5, r.get("sector_bonus", 0)))
        r["cb_score"] = min(100, raw + rs_add + sec_add)

    # D등급으로 떨어진 종목 제거
    results = [r for r in results if r.get("grade") != "D"]

    # ── combo_ok: 기관급 최종 조합 필터 (#10) ─────────────
    for r in results:
        # combo_ok: CORE(A+) 이상 + 핵심 패턴 확인 (S는 경고 등급이므로 제외)
        r["combo_ok"] = bool(
            r.get("grade") in ("A+", "A") and
            (r.get("vc_confirmed") or r.get("first_breakout") or
             r.get("retest")       or r.get("is_ath")          or
             r.get("stage1_ok")    or r.get("rs_top20")         or
             r.get("is_sector_leader"))
        )

    # 최종 재정렬: PRIME-S → PRIME → CORE(A+) → WATCH(A) → EARLY(B)
    def _sort_key(x):
        if x.get("is_prime_s"):        tier = 0
        elif x.get("is_prime"):        tier = 1
        elif x.get("grade") == "A+":   tier = 2
        elif x.get("grade") == "A":    tier = 3
        else:                           tier = 4   # B
        return (tier, -x["score"])
    results.sort(key=_sort_key)

    return results


