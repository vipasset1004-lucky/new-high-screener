# 갭 필터 — 진입 당일 갭 상태 판단
# ═══════════════════════════════════════════════════════════
def check_gap_at_open(ticker):
    """갭 상승/하락 필터 — 진입 조건 판단 (07:00 빠른 재스캔 시 호출)"""
    try:
        import yfinance as yf
        t = ticker if str(ticker).endswith(".KS") else str(ticker) + ".KS"
        df = yf.download(t, period="5d", progress=False, auto_adjust=True)
        if df is None or len(df) < 2:
            return {"status": "PENDING", "reason": "데이터 없음", "gap_pct": 0}
        yesterday_close = float(df["Close"].iloc[-2])
        today_open      = float(df["Open"].iloc[-1])
        gap_pct = round((today_open - yesterday_close) / yesterday_close * 100, 1)
        if gap_pct > 5:
            return {"status": "GAP_AVOID",  "reason": f"갭 +{gap_pct}%, 진입 보류",    "gap_pct": gap_pct}
        elif gap_pct > 3:
            return {"status": "GAP_REDUCE", "reason": f"갭 +{gap_pct}%, 사이즈 1/2",  "gap_pct": gap_pct}
        elif gap_pct < -3:
            return {"status": "GAP_DOWN",   "reason": f"갭 {gap_pct}%, 진입 검토",     "gap_pct": gap_pct}
        else:
            return {"status": "GAP_OK",     "reason": "정상 진입",                     "gap_pct": gap_pct}
    except Exception as e:
        return {"status": "PENDING", "reason": str(e), "gap_pct": 0}


