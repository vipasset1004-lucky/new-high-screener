# 진입 점수 — 100점 만점 엔트리 강도 계산
# ═══════════════════════════════════════════════════════════
def calculate_entry_score(stock):
    """진입 점수 100점 만점 (등급 40 + K-NN 30 + 갭 20 + 추가신호 10)"""
    score = 0
    breakdown = {}

    # ── [1] 등급 점수 (40점) ──────────────────────────────
    is_ps  = stock.get("is_prime_s", False)
    ps_sub = stock.get("prime_s_sub") or ""
    is_prime = stock.get("is_prime", False)
    g = stock.get("grade", "")

    # ── [1] 등급 점수 (40점) ─────────────────────────────
    if   is_ps and ps_sub == "MAX":        g_sc = 40
    elif is_ps and ps_sub == "eXplosive":  g_sc = 35
    elif is_ps and ps_sub == "Safe":       g_sc = 30
    elif is_prime and g == "A+":           g_sc = 20
    elif is_prime and g == "A":            g_sc = 15
    elif is_prime:                         g_sc = 10
    else:                                  g_sc = 0
    breakdown["grade"] = g_sc
    score += g_sc

    # ── [2] K-NN 예측 점수 (30점) ─────────────────────────
    pred = stock.get("predict") or {}
    rec  = pred.get("rec", "")
    n    = pred.get("n", 0)
    if   rec == "BUY"   and n >= 50:  p_sc = 30
    elif rec == "BUY"   and n >= 30:  p_sc = 25
    elif rec == "BUY":                p_sc = 15
    elif rec == "WATCH" and n >= 30:  p_sc = 10
    elif rec == "WATCH":              p_sc = 5
    else:                             p_sc = 0
    breakdown["predict"] = p_sc
    score += p_sc

    # ── [3] 갭 점수 (20점) — 데이터 없으면 정상(20) 가정 ──
    gap    = stock.get("gap_status") or {}
    gs     = gap.get("status", "")
    gp     = gap.get("gap_pct", 0)
    if   gs == "GAP_AVOID":  gap_sc = 0
    elif gs == "GAP_REDUCE":
        gap_sc = 10
    elif gs == "GAP_DOWN":
        if gp < -3:          gap_sc = 5
        else:                gap_sc = 15
    elif gs == "GAP_OK":
        if   -1 <= gp <= 2:  gap_sc = 20
        elif gp <= 3:        gap_sc = 15
        else:                gap_sc = 10
    else:
        gap_sc = 20          # PENDING / 데이터 없음 → 정상 가정
    breakdown["gap"] = gap_sc
    score += gap_sc

    # ── [4] 추가 신호 (10점) ─────────────────────────────
    extra = 0
    if stock.get("vc_confirmed"):                    extra += 3   # 거래량 dry-up
    ma20d = stock.get("ma20_dist_pct")
    if ma20d is not None and 0 <= ma20d <= 3:        extra += 3   # 20일선 지지
    sd  = stock.get("score_detail") or {}
    pen = -(sd.get("penalty", 0))                                 # stored as -pen
    if pen == 0:                                     extra += 4   # 페널티 없음
    breakdown["extra"] = extra
    score += extra

    # 참고: supply / failed_breakout / rs_line_high 등 정보는 stock에 보존되어 있으나
    # 백테스트 결과 (1125건) entry_score 포함 시 정확성 검증 실패하여 점수 제외.
    # UI에는 표시되어 사용자 판단 정보로 활용됨.

    # ── 액션 결정 ─────────────────────────────────────────
    if   score >= 85:  action = "STRONG_BUY"
    elif score >= 70:  action = "BUY"
    elif score >= 55:  action = "SMALL_BUY"
    elif score >= 40:  action = "HOLD"
    else:              action = "AVOID"

    return {"score": score, "action": action, "breakdown": breakdown}


def add_entry_scores(results):
    """enrich_with_predict 이후 진입 점수 일괄 추가"""
    for r in results:
        try:
            es = calculate_entry_score(r)
            r["entry_score"]     = es["score"]
            r["entry_action"]    = es["action"]
            r["entry_breakdown"] = es["breakdown"]
        except Exception:
            r["entry_score"]     = 0
            r["entry_action"]    = "AVOID"
            r["entry_breakdown"] = {}
    return results


