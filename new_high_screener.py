# STEP 5  돌파 수급 (RVOL + 최초 급증)
# ════════════════════════════════════════��═══════════════
def score_breakout_supply(nh):
    """
    O'Neil: RVOL 1.5x 최소, 2.0x 이상 권장
    Weinstein: 돌파일 거래량 2~3x 이상
    첫 급증이면 +보너스 (최초 세력 진입 신호)
    (20점)
    """
    rvol      = nh.get("rvol", 1.0)
    first_fv  = nh.get("first_vol_surge", False)

    # RVOL 점수 — 1.5~3.5x가 최적, 4x+ 극단값은 오히려 단기 과열 가능
    if   rvol >= 4.0: base = 14  # 극단적 급등 — 단기 되돌림 위험
    elif rvol >= 3.0: base = 18
    elif rvol >= 2.5: base = 17
    elif rvol >= 2.0: base = 14
    elif rvol >= 1.5: base = 9
    elif rvol >= 1.2: base = 4
    else:             base = 0

    # 최초 거래대금 급증 보너스
    bonus = 3 if (first_fv and rvol >= 1.5) else 0

    # 갭 업 보너스 — 진공 지대 갭 돌파는 강한 매수 신호
    gap_pct   = nh.get("gap_pct", 0)
    gap_bonus = 0
    if   gap_pct >= 5: gap_bonus = 5
    elif gap_pct >= 3: gap_bonus = 4
    elif gap_pct >= 2: gap_bonus = 3
    elif gap_pct >= 1: gap_bonus = 2
    elif gap_pct >= 0: gap_bonus = 1

    # 캔들 품질 보너스 (detect_new_high에서 계산)
    candle_bonus = nh.get("candle_score", 0)

    total = base + bonus + gap_bonus + candle_bonus
    detail = {
        "rvol": round(rvol, 2), "first_vol": first_fv,
        "gap_pct": round(gap_pct, 2), "gap_bonus": gap_bonus,
        "candle_bonus": candle_bonus,
    }
    return min(total, 20), detail


