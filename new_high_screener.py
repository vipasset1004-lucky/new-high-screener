import urllib.request as _urlreq
import ast as _ast

def _fetch_naver_chart(symbol, days):
    """네이버 금융 차트 API — 종목/지수 공통. OHLCV + 외국인 소진율 반환."""
    end   = datetime.now()
    start = end - timedelta(days=days + 60)
    url = (f"https://api.finance.naver.com/siseJson.naver"
           f"?symbol={symbol}&requestType=1"
           f"&startTime={start.strftime('%Y%m%d')}"
           f"&endTime={end.strftime('%Y%m%d')}"
           f"&timeframe=day")
    try:
        req = _urlreq.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with _urlreq.urlopen(req, timeout=15) as r:
            text = r.read().decode("utf-8").strip()
        data = _ast.literal_eval(text)  # [헤더, ...rows]
        if not data or len(data) < 2:
            return None
        rows = data[1:]
        df = pd.DataFrame(rows, columns=["Date","Open","High","Low","Close","Volume","ForeignPct"])
        df["Date"] = pd.to_datetime(df["Date"], format="%Y%m%d")
        df = df.set_index("Date")
        for col in ["Open","High","Low","Close","Volume","ForeignPct"]:
            df[col] = pd.to_numeric(df[col], errors="coerce")
        df = df.dropna(subset=["Close","Volume"])
        # 거래정지일 제거: 네이버는 거래정지일에 Open=High=Low=Volume=0, Close만 직전종가로 채움
        df = df[(df["Open"] > 0) & (df["High"] > 0) & (df["Low"] > 0) & (df["Close"] > 0)]
        return df if len(df) > 0 else None
    except Exception:
        return None

def fetch_daily(ticker, is_korean=True, days=SCAN_DAYS):
    # 한국 종목: 네이버 우선 (휴장일/EOD 지연 없음, 외국인 보유율 보너스)
    if is_korean:
        df = _fetch_naver_chart(str(ticker).zfill(6), days)
        if df is not None and len(df) >= 120:
            return df
    # Fallback: yfinance (미국 종목 또는 네이버 실패 시)
    suffixes = [".KS", ".KQ"] if is_korean else [""]
    for sfx in suffixes:
        try:
            sym = f"{ticker}{sfx}"
            start = datetime.now() - timedelta(days=days + 120)
            df = yf.download(sym, start=start, end=datetime.now(),
                             auto_adjust=True, progress=False, timeout=15)
            if df is None or len(df) < 120: continue
            if isinstance(df.columns, pd.MultiIndex):
                df.columns = df.columns.get_level_values(0)
            df = df.dropna(subset=["Close","Volume"])
            if df["Close"].iloc[-1] <= 0: continue
            return df
        except: continue
    return None

# ── 외국인/기관 매매 동향 (네이버 모바일 trend API) ──
def fetch_supply_trend(ticker, days=10):
    """
    한국 종목 외국인/기관/개인 일별 순매수 데이터 (네이버 모바일).
    Returns: dict {
        'foreigner_5d': 외국인 5일 누적 순매수 (주),
        'organ_5d':     기관 5일 누적 순매수 (주),
        'foreign_inst_combo_days': 외국인+기관 동반매수 일수 (5일 중),
        'foreign_buy_days': 외국인 매수일수 (5일 중),
    }
    """
    try:
        url = f"https://m.stock.naver.com/api/stock/{str(ticker).zfill(6)}/trend"
        req = _urlreq.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with _urlreq.urlopen(req, timeout=8) as r:
            data = json.loads(r.read().decode("utf-8"))
        if not data: return None
        # 최근 5일 추출 (응답은 최신순 정렬)
        recent = data[:5]

        def _parse(s):
            if not s: return 0
            return int(str(s).replace(",", "").replace("+", ""))

        f5 = sum(_parse(d.get("foreignerPureBuyQuant", "0")) for d in recent)
        o5 = sum(_parse(d.get("organPureBuyQuant", "0"))     for d in recent)
        combo = sum(1 for d in recent
                    if _parse(d.get("foreignerPureBuyQuant","0")) > 0
                    and _parse(d.get("organPureBuyQuant","0")) > 0)
        fbuy = sum(1 for d in recent
                   if _parse(d.get("foreignerPureBuyQuant","0")) > 0)
        obuy = sum(1 for d in recent
                   if _parse(d.get("organPureBuyQuant","0")) > 0)

        return {
            "foreigner_5d": f5,
            "organ_5d": o5,
            "foreign_inst_combo_days": combo,
            "foreign_buy_days": fbuy,
            "organ_buy_days": obuy,
        }
    except Exception:
        return None


def fetch_supply_batch(tickers, max_workers=15):
    """후보 종목 외국인/기관 매매 동향 병렬 수집. tickers: list of dicts"""
    from concurrent.futures import ThreadPoolExecutor, as_completed
    out = {}
    with ThreadPoolExecutor(max_workers=max_workers) as ex:
        futures = {ex.submit(fetch_supply_trend, t["ticker"]): t for t in tickers}
        for fut in as_completed(futures):
            try:
                t = futures[fut]
                supply = fut.result()
                if supply:
                    out[t["ticker"]] = supply
            except Exception:
                pass
    return out


# ── Failed Breakout 감지 (Minervini) ──
def is_failed_breakout(df, nh):
    """첫 돌파 후 8~10거래일 내 -8% 이상 빠지면 실패. True = 위험."""
    if df is None or nh is None: return False
    if not nh.get("first_breakout"): return False
    days_ago = nh.get("days_ago", 0)
    if days_ago < 1 or days_ago > 10: return False
    breakout_high = float(nh.get("breakout_high", 0))
    if breakout_high <= 0: return False
    close = df["Close"].values
    n_check = min(days_ago + 1, len(close))
    since_high = close[-n_check:]
    if since_high.min() / breakout_high - 1 <= -0.08:
        return True
    return False


# ── RS Line 신고가 (Minervini 핵심) ──
def is_rs_line_at_high(df, index_df):
    """RS line = stock_close / index_close가 52주 신고가에 도달했는지.
    가격은 신고가 못 쳐도 RS가 신고가면 진짜 주도주 신호."""
    if df is None or index_df is None: return False
    c = df["Close"].values
    if len(c) < 50: return False
    # 인덱스를 종목 길이에 맞춤
    i_close = index_df["Close"]
    aligned = i_close.reindex(df.index, method="ffill").values
    if len(aligned) != len(c): return False
    valid = ~pd.isna(aligned) & (aligned > 0)
    if valid.sum() < 50: return False
    rs = c[valid] / aligned[valid]
    if len(rs) < 50: return False
    return rs[-1] >= rs.max() * 0.99


# ── Distribution Day 카운트 (O'Neil 시장 진단) ──
def count_distribution_days(index_df, lookback=25):
    """25거래일 내 분산일 (큰 거래량 + 종가 -0.2%↓) 횟수.
    0~3: 정상 / 4~5: 주의 / 6+: 위험"""
    if index_df is None or len(index_df) < lookback + 1: return 0
    c = index_df["Close"].values
    v = index_df["Volume"].values
    v_ma = pd.Series(v).rolling(20).mean().values
    cnt = 0
    for i in range(-lookback, 0):
        if i - 1 < -len(c) or pd.isna(v_ma[i]): continue
        if c[i-1] == 0: continue
        ret = c[i] / c[i-1] - 1
        if ret < -0.002 and v[i] > v_ma[i] * 1.05:
            cnt += 1
    return cnt


# ── 1차 필터: 신고가 후보 판별 (detect_new_high와 동기화) ──
def is_near_high_candidate(df, win=None, lookback=None):
    """
    1차 필터 — detect_new_high가 신호 낼 가능성 있는 모든 종목 통과.
    수학적으로 detect_new_high의 신호 조건과 동일 로직을 사용해 놓침 0 보장.
    조건: 최근 win+1일 안의 max(high) >= 그 이전 lookback일의 max(high) * 0.99
    """
    if df is None: return False
    win      = win      or NEW_HIGH_WIN
    lookback = lookback or HIGH_52W
    if len(df) < lookback + win:
        return True   # 데이터 부족 종목은 보수적으로 통과 (신규상장 등)
    high = df["High"].values
    high_recent = float(high[-(win+1):].max())
    high_prev   = float(high[-(lookback+win):-(win+1)].max())
    if high_prev <= 0: return True
    return high_recent >= high_prev * 0.99   # 1% 안전 마진


# ── 병렬 OHLCV 수집 ──
def fetch_daily_batch(tickers_meta, max_workers=15, is_korean=True, progress_cb=None):
    """
    여러 종목 OHLCV를 병렬로 수집. tickers_meta: list of {ticker, name, themes...}
    return: dict {ticker: df}
    progress_cb(done, total): 콜백 (선택)
    """
    from concurrent.futures import ThreadPoolExecutor, as_completed
    out = {}
    total = len(tickers_meta)

    def _one(t):
        return t["ticker"], fetch_daily(t["ticker"], is_korean=is_korean)

    with ThreadPoolExecutor(max_workers=max_workers) as ex:
        futures = {ex.submit(_one, t): t for t in tickers_meta}
        done = 0
        for fut in as_completed(futures):
            try:
                tk, df = fut.result()
                if df is not None:
                    out[tk] = df
            except Exception:
                pass
            done += 1
            if progress_cb and (done % 20 == 0 or done == total):
                progress_cb(done, total)
    return out


